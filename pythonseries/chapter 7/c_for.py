#range function
for i in range (0,100,4):
    print(i)
#for loop for list
l = [1,4,5,6,7,8]
for i in l :
    print(i)

#for loop for tuple
t = (1,3,5)
for i in t:
    print(i)

#for loop for string
s = "harry"
for i in s:
    print(i)

#for loop with else
l = [7,8,9]
for item in l:
    print(item)

else:
    print("done")